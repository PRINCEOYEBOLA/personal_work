# Get the attendance for each week
week1 = int(input("Enter attendance for week 1: "))

week2 = int(input("\nEnter attendance for week 2: "))

week3 = int(input("\nEnter attendance for week 3: "))

week4 = int(input("\nEnter attendance for week 4: "))

week5 = int(input("\nEnter attendance for week 5: "))

# Calculate the average
average = (week1 + week2 + week3 + week4 + week5) / 5

# Print out the average attendance
print("\nThe average attendance is: ", average)